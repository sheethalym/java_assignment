package com.xworkz.oops.encapsulation;

public class Myself {
	
	String name;
	int height;
	byte age;
	long phno;
	int birthYear;
	short id;
	String favouriteGame;
	String food;
	int bankbal;
	int salary;
	
	public Myself() {
		
	}
	
	public void setByName(String name){
		this.name=name;
	}
	
	public String getByName() {
		return name;
	}
	
	public void setByHeight(int height ) {
		this.height = height;
	}
	
	public int getByHeight() {
		return height;
	}
	
	public void setByAge(byte age) {
		this.age = age;
	}
	
	public byte getByAge() {
		return age;
	}
	
	public void setByPhno(long phno ) {
		this.phno = phno;
	}
	
	public long getByPhno() {
		return phno;
	}
	
	public void setByBirthYear(int birthYear ) {
		this.birthYear = birthYear;
	}
	
	public int getByBirthYear() {
		return birthYear;
	}
	
	public void setById(short id ) {
		this.id = id;
	}
	
	public short getById() {
		return id;
	}
	
	public void setByFavouriteGame(String favouriteGame){
		this.favouriteGame=favouriteGame;
	}
	
	public String getByFavouriteGame() {
		return favouriteGame;
	}
	
	public void setByFood(String food){
		this.food=food;
	}
	
	public String getByFood() {
		return food;
	}
	
	public void setByBankbal(int bankbal) {
		this.bankbal = bankbal;
	}
	
	public int getByBankbal() {
		return bankbal;
	}
	
	public void setBySalary(int salary ) {
		this.salary = salary;
	}
	
	public int getBySalary() {
		return salary;
	}

}
